fn main() {
    let out_dir = std::path::PathBuf::from(std::env::var_os("OUT_DIR").unwrap());
    let ico_path = out_dir.join("icon.ico");
    let png_path = out_dir.join("icon.png");

    std::fs::write(&ico_path, MINIMAL_ICO).expect("failed to write generated window icon");
    std::fs::write(&png_path, MINIMAL_PNG).expect("failed to write generated app icon");

    let tauri_config = format!(
        r#"{{"bundle":{{"icon":["{}","{}"]}}}}"#,
        json_path(&ico_path),
        json_path(&png_path)
    );
    std::env::set_var("TAURI_CONFIG", &tauri_config);
    println!("cargo:rustc-env=TAURI_CONFIG={tauri_config}");

    tauri_build::build();
}

fn json_path(path: &std::path::Path) -> String {
    path.display().to_string().replace('\\', "\\\\")
}

